     private void validaTam(JTextField txt)throws ColaException{
        if(txtTam.getText().length()==0)throw new ColaException("Campo vac√≠o");
        String c = txt.getText();
        for (int i = 0; i < c.length(); i++) {
            if(c.charAt(i)<48 || c.charAt(i)>57) throw new ColaException("Solo n√∫meros");
        } 
    }
     private void validaDato(JTextField txt)throws ColaException{
        if(txt.getText().length()==0){txtDat.requestFocus(); throw new ColaException("Campo vac√≠o");}
    }
    
    private boolean validado(JTextField t){
        try{
          validaTam(t);  
        }catch(ColaException err){
            showMessageDialog(this, err.getMessage());
            txtTam.setText("");
            return false;
        }
        
        return true;
    }
    private boolean dValidado(JTextField t){
        try{
          validaDato(t);  
        }catch(ColaException err){
            showMessageDialog(this, err.getMessage());
            return false;
        }
        
        return true;
    }
    

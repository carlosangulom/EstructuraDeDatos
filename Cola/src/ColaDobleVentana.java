    private void setColmRend(){
        centerRenderer.setHorizontalAlignment( SwingConstants.CENTER );
        for (int i = 0; i < col.getTam(); i++) {
            tblT.getColumnModel().getColumn(i).setCellRenderer( centerRenderer );
        }
    }
    
    private void eliminar(){
        for (int i = 0; i < col.getTam(); i++) {
            tblT.setValueAt("", 0, i);
        }
        
        setColmRend();
        
        col.setDerc(col.getTam()/2);
        col.setIzq(col.getTam()/2);
        tblT.setValueAt("|||", 0, col.getTam()/2);
    }
    

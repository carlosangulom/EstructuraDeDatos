    private void btnHashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHashActionPerformed
       int pos =hash (txtClave.getText());
       if(m.getValueAt(pos,0)==null){showMessageDialog(this,"NO existe"); return;}
       while(!m.getValueAt(pos, 0).toString().equals(txtClave.getText())){
          pos++; 
          if(m.getValueAt(pos,0)==null){showMessageDialog(this,"NO existe"); return;}
       }
       
       txtDescrip.setText(m.getValueAt(pos,1).toString()); 
       txtPrecio.setText(m.getValueAt(pos,2).toString()); 
                 
    }//GEN-LAST:event_btnHashActionPerformed


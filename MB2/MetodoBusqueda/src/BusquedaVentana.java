    public int hash(String c){
        int s=0;
        for(int i=0; i<c.length(); i++) s+=c.charAt(i);
        return s%100; 
    }
    
    private void btnGuardarOtroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarOtroActionPerformed
       int pos =hash (txtClave.getText());
       if(!m.getValueAt(pos, 0).toString().equals("") && !m.getValueAt(pos, 1).toString().equals(txtClave.getText()))pos++; 
       while(!m.getValueAt(pos, 0).toString().equals(""))pos++; 
       m.setValueAt(txtClave.getText(), pos, 0); 
       m.setValueAt(txtDescrip.getText(), pos, 1);
       m.setValueAt(txtPrecio.getText(), pos, 2);
       txtClave.setText(""); 
       txtDescrip.setText(""); 
       txtPrecio.setText(""); 
       
    }//GEN-LAST:event_btnGuardarOtroActionPerformed
    public void guardarArchivo(){
          try { 
            fs=new java.io.FileWriter("Articulo.txt");
              for (int i = 0; i < m.getRowCount(); i++) {
                  if(m.getValueAt(i, 0)!=null)
                  a=new Articulo(m.getValueAt(i, 0).toString(),m.getValueAt(i, 1).toString(),Float.parseFloat(m.getValueAt(i, 2).toString()));
                  else a=new Articulo ("","",0);
                  a.guardar(fs);
                  
              }
        } catch (IOException ex) {
             showMessageDialog(this, ex.getMessage());
        }
        
    }
    
    public void leerArchivo(){
        try {
            fe = new java.io.FileReader("Articulo.txt");
            br=new java.io.BufferedReader(fe);
            String reg="";
            
            for (int i = 0; i < 100; i++) {
                reg=br.readLine();
                String R[] = reg.split("\\|");
                m.setValueAt(R[0], i, 0);
                m.setValueAt(R[1], i, 1);
                m.setValueAt(R[2], i, 2);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(BusquedaVentana.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(BusquedaVentana.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
